#include <iostream>
#include <cstring>

using namespace std;
const int N=30;
char g[N][N];
int dx[4]={0,1,0,-1};//右 下 左 上
int dy[4]={1,0,-1,0};
int st1,st2;
int ed1,ed2;
int used[N][N]; //一定要注意是否走过 图的问题
bool dfs(int s1 ,int s2)
{
    if(s1==ed1&&s2==ed2){return true;}
    int i;
    for(i=0;i<=3;i++)
    {
        int x=s1+dx[i], y=s2+dy[i];
        if(x>=0&& x<=19&& y>=0&& y<=19 &&g[x][y]=='.' &&used[x][y]==0)
        {
            used[x][y]=1;
            if(dfs(x,y)) return 1;
            // used[x][y]=0;    // 不用回溯 走到就是到了
        }
    }
return 0;
}

int main()
{
    int n;
    cin>>n;
    while(n--)
    {
        memset (used,0,sizeof used);  //还原！
        cin>>st1>>st2>>ed1>>ed2;
        int i,j;
        for(i=0;i<=19;i++)
        {
            for(j=0;j<=19;j++)
            {
                cin>>g[i][j];   
            }
        }
        used[st1][st2]=1;
        if (dfs(st1 ,st2 )) {cout<<"Yes"<<endl;}
        else {cout<<"No"<<endl;}
        
    }  
    return 0;
}
